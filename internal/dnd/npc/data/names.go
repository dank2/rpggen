// GetName returns a name from the table based on index
func GetName(gender, race string) string {
	switch race {
	case "Human":
		return getHumanName(gender)
	case "Half-orc":
		return getHalforcName(gender)
	case "Dragonborn":
		return getDragonbornName(gender)
	case "Dwarf":
		return getDwarfName(gender)
	case "Halfling":
		return getHalflingName(gender)
	case "Gnome":
		return getGnomeName(gender)
	case "Elf":
		return getElfName(gender)
	case "Half-elf":
		return getHalfelfName(gender)
	default:
		return NamesNorseMale[random.Intn(len(NamesNorseMale))]
	}
}

